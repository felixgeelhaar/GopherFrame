func arrowTypeString(dt arrow.DataType) string {
	switch dt.ID() {
	case arrow.FLOAT64:
		return "float64"
	case arrow.INT64:
		return "int64"
	case arrow.STRING:
		return "string"
	case arrow.BOOL:
		return "bool"
	default:
		return "interface{}"
	}
}

func arrowToGoType(dt arrow.DataType) string {
	return arrowTypeString(dt)
}

func arrowToBuilderFn(dt arrow.DataType) string {
	switch dt.ID() {
	case arrow.FLOAT64:
		return "array.NewFloat64Builder"
	case arrow.INT64:
		return "array.NewInt64Builder"
	case arrow.STRING:
		return "array.NewStringBuilder"
	case arrow.BOOL:
		return "array.NewBooleanBuilder"
	default:
		return "array.NewStringBuilder"
	}
}

// --- UDF Serialization ---

// SerializableUDF represents a UDF that can be serialized for distributed execution.
type SerializableUDF struct {
	Name       string
	InputCols  []string
	OutputType string
	Code       string // Go source code of the UDF for remote compilation
}

func init() {
	gob.Register(&SerializableUDF{})
}

// SerializeUDF serializes a UDF specification to bytes for network transfer.
func SerializeUDF(name string, inputCols []string, outputType arrow.DataType, code string) ([]byte, error) {
	udf := &SerializableUDF{
		Name:       name,
		InputCols:  inputCols,
		OutputType: arrowTypeString(outputType),
		Code:       code,
	}

	var buf bytes.Buffer
	enc := gob.NewEncoder(&buf)
	if err := enc.Encode(udf); err != nil {
		return nil, fmt.Errorf("failed to serialize UDF: %w", err)
	}
	return buf.Bytes(), nil
}

// DeserializeUDF deserializes a UDF specification from bytes.
func DeserializeUDF(data []byte) (*SerializableUDF, error) {
	var udf SerializableUDF
	dec := gob.NewDecoder(bytes.NewReader(data))
	if err := dec.Decode(&udf); err != nil {
		return nil, fmt.Errorf("failed to deserialize UDF: %w", err)
	}
	return &udf, nil
}


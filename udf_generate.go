// GenerateUDFCode generates optimized Go source code for a typed UDF.
// This enables go:generate workflows where UDFs are compiled into native functions.
//
// Usage in go:generate workflow:
//
//	//go:generate go run gen_udf.go
//
// The generated code avoids interface{} boxing and runtime type assertions.
func GenerateUDFCode(name string, inputCols []string, inputTypes, outputType arrow.DataType) (string, error) {
	tmpl, err := template.New("udf").Parse(udfTemplate)
	if err != nil {
		return "", fmt.Errorf("template parse error: %w", err)
	}

	data := struct {
		Name       string
		InputCols  []string
		OutputType string
		GoType     string
		BuilderFn  string
	}{
		Name:       name,
		InputCols:  inputCols,
		OutputType: arrowTypeString(outputType),
		GoType:     arrowToGoType(outputType),
		BuilderFn:  arrowToBuilderFn(outputType),
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("template execute error: %w", err)
	}
	return buf.String(), nil
}

